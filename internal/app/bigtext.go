const miniCharWidth = 3  // pixels per character
const miniCharHeight = 4 // pixel rows per character
const miniOutRows = 2    // output rows (4 pixel rows → 2 half-block rows)

// miniTextWidth returns the display width of mini text (including spacing).
func miniTextWidth(s string) int {
	n := 0
	for _, ch := range s {
		if ch >= 'a' && ch <= 'z' {
			ch = ch - 'a' + 'A'
		}
		if _, ok := miniFont[ch]; ok {
			if n > 0 {
				n++ // 1-col spacing between chars
			}
			n += miniCharWidth
		}
	}
	return n
}

// renderMiniText renders text using the compact 3×2 half-block font.
func renderMiniText(buf *Buffer, startX, startY int, text string, fg, bg color.Color) {
	x := startX
	for _, ch := range text {
		if ch >= 'a' && ch <= 'z' {
			ch = ch - 'a' + 'A'
		}
		glyph, ok := miniFont[ch]
		if !ok {
			glyph = miniFont[' ']
		}

		for outRow := 0; outRow < miniOutRows; outRow++ {
			topPixelRow := outRow * 2
			botPixelRow := topPixelRow + 1

			for col := 0; col < miniCharWidth; col++ {
				bit := uint8(1 << (miniCharWidth - 1 - col))
				topOn := glyph[topPixelRow]&bit != 0
				botOn := false
				if botPixelRow < miniCharHeight {
					botOn = glyph[botPixelRow]&bit != 0
				}

				var c rune
				switch {
				case topOn && botOn:
					c = '█'
				case topOn && !botOn:
					c = '▀'
				case !topOn && botOn:
					c = '▄'
				default:
					c = ' '
				}

				bx := x + col
				by := startY + outRow
				if bx >= 0 && bx < buf.Width && by >= 0 && by < buf.Height {
					buf.Cells[by][bx] = Cell{Char: c, Fg: fg, Bg: bg}
				}
			}
		}

		x += miniCharWidth + 1
	}
}

