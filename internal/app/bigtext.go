// renderBigText renders text as double-height block characters into the buffer.
// Uses half-block characters (▀▄█) for a 4×3 output per character.
func renderBigText(buf *Buffer, startX, startY int, text string, fg, bg color.Color) {
	x := startX
	for _, ch := range text {
		// Convert to uppercase
		if ch >= 'a' && ch <= 'z' {
			ch = ch - 'a' + 'A'
		}
		glyph, ok := bigFont[ch]
		if !ok {
			glyph = bigFont[' '] // fallback to space
		}

		// Render 3 output rows from 5 pixel rows
		for outRow := 0; outRow < bigOutRows; outRow++ {
			topPixelRow := outRow * 2
			botPixelRow := topPixelRow + 1

			for col := 0; col < bigCharWidth; col++ {
				bit := uint8(1 << (bigCharWidth - 1 - col))
				topOn := glyph[topPixelRow]&bit != 0
				botOn := false
				if botPixelRow < bigCharHeight {
					botOn = glyph[botPixelRow]&bit != 0
				}

				var ch rune
				switch {
				case topOn && botOn:
					ch = '█'
				case topOn && !botOn:
					ch = '▀'
				case !topOn && botOn:
					ch = '▄'
				default:
					ch = ' '
				}

				bx := x + col
				by := startY + outRow
				if bx >= 0 && bx < buf.Width && by >= 0 && by < buf.Height {
					buf.Cells[by][bx] = Cell{Char: ch, Fg: fg, Bg: bg}
				}
			}
		}

		x += bigCharWidth + 1 // 1-col spacing
	}
}
